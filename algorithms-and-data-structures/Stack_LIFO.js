// EXPLANATION
// 	Stack - LIFO Data Structure. Last-In-First-Out,
// 	stores objects into a sort of vertical tower of elements, where you may only add & take/remove from the top
// 	to add    = push('ELEMENT') to add to the top 
// 	to remove = pop() to remove from the top
// 	uses      = in screenshots

/* =-=-=-=-=-=-=-=-=-=-= */
var stack = [];

// To add an item to the stack.
stack.push('Stack Element 1');
stack.push('Stack Element 2');

// To remove an item from the stack.
stack.pop();

console.log(stack);